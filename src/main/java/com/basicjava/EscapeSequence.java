package com.basicjava;

public class EscapeSequence {
	public static void main(String[] args) {
//		To  insert the tab \t
		System.out.println("hello\tjava");
		System.out.println("hii\"java\"welcome");
//		To insert the backspace \b
		System.out.println("hii\b welcome");
//		To insert the new line
		System.out.println("hii\n welcome");
//		To carrying return the text point
		System.out.println("hii\r welcome");
		
//	To insert formfeed text this point	
		System.out.println("hii\f welcome");
		
//		To insert the single character at this point
		System.out.println("hii\' welcome");
		
//		To insert the double quotes at this point
		System.out.println("hii\" welcome");
		
//		To insert the backslash at this point
		System.out.println("hii\\ welcome");
		
	}

}
